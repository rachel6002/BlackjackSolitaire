public class Card {

	private int rank;
	private int suit;

	public Card(int rank, int suit) { // constructor for the card
		this.rank = rank;
		this.suit = suit;
	}

	public int getRank() { // to get rank of card
		return rank;
	}

	public int getSuit() { // to get suit of card
		return suit;

	}

}
